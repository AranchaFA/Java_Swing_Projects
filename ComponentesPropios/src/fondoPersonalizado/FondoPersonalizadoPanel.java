    public PropiedadesFondo getPropiedadesFondo(){
        //PropiedadesFondo propiedadesFondo=new PropiedadesFondo();
        // Habr√≠a que comprobar que la ruta del textField o el color sean correctos
        if (jTF_imagen.getText()!=null&&jTF_imagen.getText()!="") {            
            File ficheroSeleccionado=new File(jTF_imagen.getText());
            PropiedadesFondo propiedadesFondo=new PropiedadesFondo(ficheroSeleccionado);
            return propiedadesFondo;
        } else {
            Color colorSeleccionado=new Color(Integer.valueOf(jTF_color.getText()));
            PropiedadesFondo propiedadesFondo=new PropiedadesFondo(colorSeleccionado);
            return propiedadesFondo;
        }
    }

